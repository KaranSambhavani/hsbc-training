package Empfolder2;

public class Emp {
	private int empId;
	private String empName;
	public Emp()
	{
		
	}
	
	public Emp(int empId, String empName) {
		super();
		this.empId = empId;
		this.empName = empName;
	}
	public int getEmpId() {
		return empId;
	}
	
	public String getEmpName() {
		return empName;
	}
	
	

}
